// Condições

// Maior
let a = 2 > 0 // true

// Maior ou igual
let b = 2 >= 2 // true

// Menor
let c = 2 < 0 // true

// Menor
let d = 2 <= 0  // true

// Igual
let e = 2 == '2' // true

// Igual em valor e tipo
let f = 2 === "2" // false
let g = 2 === 2 // true

// Diferente
let h = 2 != 1 // true

// Diferente em valor ou tipo 
let i = "2" !== 2 // false

// Operadores Lógicos:

//  e => &&
if ( 2>0 && 1!==1){
    console.log('false');
} else {
    console.log('true');
}
//  ou => ||
if (2>0 || 1!==1 ){
    console.log('true');
} else {
    console.log('false');
}
//  não => !
