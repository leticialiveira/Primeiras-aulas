// Laços de repetição:

// while => executa um código enquanto uma condição for verdadeira.
let count = 0;
while(count<100){
    console.log(count);
    count ++;
} 

// do-while =>usado em casos que é  necessário executar o while pelo menos uma vez.
// Primeiro se executa o código e depois é verificado a condição para continuar executando ou não.

let countt = 0;
do{
    console.log(countt);
    countt++;
}
while(countt < 100)


// For => é declarado um valor inicial, depois é determinado a condição de parada e por fim a passo dado entre o valor inicial e o final. 
 
